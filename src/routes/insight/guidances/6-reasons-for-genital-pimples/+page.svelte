		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Molluscum contagiosum is a viral skin infection that causes small, raised bumps with a central
			depression. These bumps may resemble pimples and can occur in the genital area, particularly
			in sexually active individuals. Molluscum contagiosum is contagious and can be spread through
			direct contact. Medical evaluation and treatment are recommended to manage this condition
			effectively.
