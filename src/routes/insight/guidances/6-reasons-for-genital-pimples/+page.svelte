		<h3 class="font-sans font-semibold text-3xl">Genital Herpes</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Genital herpes is a sexually transmitted infection (STI) caused by the herpes simplex virus
			(HSV). It can present as painful, fluid-filled blisters or pimples that eventually rupture and
			form ulcers. Genital herpes outbreaks are typically recurring, and proper diagnosis and
			medical treatment are essential. Consult a healthcare professional for accurate diagnosis and
			management of genital herpes.
		</p>
