		<h3 class="font-sans font-semibold text-3xl">Folliculitis</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Folliculitis occurs when hair follicles become inflamed or infected. This can lead to the
			development of small, red pimples or pustules. Common causes include friction from clothing,
			shaving or waxing, or bacterial or fungal infections. Keeping the area clean and dry, wearing
			breathable clothing, and avoiding tight undergarments can help prevent folliculitis.
