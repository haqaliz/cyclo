	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Genital Warts</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Genital warts are caused by the human papillomavirus (HPV) and are a common STI. They appear
			as small, flesh-colored or pink bumps in the genital area. These warts can be raised, flat, or
			cauliflower-like in appearance. It is crucial to seek medical advice for proper diagnosis,
			treatment, and management of genital warts.
		</p>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Ingrown Hairs</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Ingrown hairs can occur when hair grows back into the skin instead of outwards. They often
			appear as small, red bumps and can be accompanied by inflammation or discomfort. Ingrown hairs
			commonly develop after shaving, waxing, or other hair removal methods. Proper exfoliation,
			gentle hair removal techniques, and avoiding tight clothing can help prevent ingrown hairs.
		</p>
