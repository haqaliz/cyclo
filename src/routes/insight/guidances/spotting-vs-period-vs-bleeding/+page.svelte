	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Spotting</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Spotting refers to light, intermittent vaginal bleeding that typically occurs between periods.
			Here are some key characteristics of spotting:
		</p>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Color and Consistency:</strong> Spotting may appear as pink, brown, or light red in color.
				It is generally lighter than a regular period and often characterized by small amounts of blood.
			</li>
			<li>
				<strong>Duration:</strong> Spotting usually lasts for a shorter duration than a regular period.
				It may occur for a few hours or persist for a few days before subsiding.
			</li>
			<li>
				<strong>Causes:</strong> Spotting can be caused by various factors, such as hormonal fluctuations,
				ovulation, contraceptive use, or implantation bleeding in early pregnancy.
			</li>
		</ol>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Menstrual Period</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			A menstrual period, also known as a menstrual cycle, is the shedding of the uterine lining in
			preparation for a potential pregnancy. Here are some key characteristics of a regular period:
		</p>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Color and Consistency:</strong> Menstrual blood is typically bright to dark red in color
				and may contain clots. It is usually more abundant than spotting.
			</li>
			<li>
				<strong>Duration:</strong> A regular period generally lasts between 3 to 7 days, although individual
				variations are common.
			</li>
			<li>
				<strong>Regularity:</strong> Menstrual periods occur in a predictable pattern, with an average
				cycle length of 28 days. However, it is normal for cycles to vary between individuals.
			</li>
		</ol>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Abnormal Bleeding</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Abnormal bleeding refers to any irregular bleeding that deviates from your typical menstrual
			pattern. Here are some signs of abnormal bleeding:
		</p>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Excessive Flow:</strong> If your bleeding is excessively heavy, requiring changing pads
				or tampons every hour or two, it may be a sign of abnormal bleeding.
			</li>
			<li>
				<strong>Prolonged Duration:</strong> If your period lasts longer than seven days or significantly
				deviates from your regular cycle, it may indicate abnormal bleeding.
			</li>
			<li>
				<strong>Irregular Timing:</strong> Bleeding that occurs outside of your regular menstrual cycle
				or occurs after menopause should be considered abnormal and requires medical attention.
			</li>
			<li>
				<strong>Intensity and Pain:</strong> Severe pain or intense cramping accompanied by heavy bleeding
				may indicate an underlying issue and should be evaluated by a healthcare professional.
			</li>
		</ol>
	</div>
