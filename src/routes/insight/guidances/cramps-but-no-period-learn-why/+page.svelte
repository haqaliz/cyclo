				<p class="font-sans text-lg text-justify">
					While mild cramps without a period can often be attributed to benign causes, there are
					instances where you should seek medical advice:
				</p>
				<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
					<li>
						<strong>Severe Pain:</strong>
						<span
							>If you experience severe, persistent, or sudden-onset pain, consult a healthcare
							professional.</span
						>
					</li>
					<li>
						<strong>Fever or Other Symptoms:</strong>
						<span
							>If your cramps are accompanied by symptoms like fever, chills, or abnormal bleeding,
							it's essential to get checked.</span
						>
					</li>
					<li>
						<strong>Irregular Cycles:</strong>
						<span
							>If your cramps are associated with irregular menstrual cycles, or you have concerns
							about your reproductive health, consult a gynecologist.</span
						>
					</li>
				</ol>
			</div>
