	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">
			Cramps vs. Period Cramps: What's the Difference?
		</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Before we dive into the possible reasons for cramps without a period, it's essential to
			distinguish between the two types of cramps:
		</p>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Menstrual Cramps:</strong>
				<span
					>These cramps, known as dysmenorrhea, typically occur just before or during your period.
					They are caused by uterine contractions that help expel the uterine lining.</span
				>
			</li>
			<li>
				<strong>Non-menstrual Cramps:</strong>
				<span
					>Non-menstrual cramps can happen at various times during your cycle and for various
					reasons. They are often not related to menstruation but can be equally uncomfortable.</span
				>
			</li>
		</ol>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Possible Causes of Cramps Without a Period</h3>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Ovulation:</strong>
				<span
					>Some people experience mild cramping, known as mittelschmerz, during ovulation. This
					occurs when the follicle releases an egg from the ovary. These cramps are typically
					short-lived.</span
				>
			</li>
			<li>
				<strong>Hormonal Fluctuations:</strong>
				<span
					>Hormonal imbalances, especially in conditions like polycystic ovary syndrome (PCOS) or
					endometriosis, can lead to pelvic pain or cramping.</span
				>
			</li>
			<li>
				<strong>Stress:</strong>
				<span
					>Emotional or physical stress can lead to muscle tension, which can manifest as cramps in
					the lower abdomen.</span
				>
			</li>
			<li>
				<strong>Digestive Issues:</strong>
				<span
					>Gastrointestinal problems like constipation or gas can often mimic period cramps and
					cause discomfort.</span
				>
			</li>
			<li>
				<strong>Pelvic Inflammatory Disease (PID):</strong>
				<span
					>An infection in the reproductive organs can cause pelvic pain and cramps. PID should be
					treated promptly.</span
				>
			</li>
			<li>
				<strong>Cysts:</strong>
				<span
					>Ovarian cysts can occasionally lead to cramping. These cysts may resolve on their own or
					require medical attention.</span
				>
			</li>
			<li>
				<strong>Adhesions or Scar Tissue:</strong>
				<span
					>Previous surgeries or infections can lead to the formation of adhesions, which can cause
					pelvic pain.</span
				>
			</li>
			<li>
				<strong>IUDs or Birth Control:</strong>
				<span
					>Some contraceptive methods, like intrauterine devices (IUDs), can lead to cramping
					initially. However, this usually subsides over time.</span
				>
			</li>
		</ol>
	</div>
