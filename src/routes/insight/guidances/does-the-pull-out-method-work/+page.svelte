				about 4 may become pregnant.
			</li>
			<li>
				<strong>Typical Use:</strong> In real-world scenarios where mistakes, incorrect timing, or hesitation
				can occur, the effectiveness drops to around 78%. This means that out of 100 couples using the
				pull-out method as their primary form of contraception for a year, about 22 may experience an
				unintended pregnancy.
			</li>
