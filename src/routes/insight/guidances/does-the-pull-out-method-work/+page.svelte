		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			Despite its simplicity, there are significant limitations to the pull-out method:
		</p>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Pre-Ejaculate:</strong> One of the main drawbacks is the presence of pre-ejaculate (pre-cum),
