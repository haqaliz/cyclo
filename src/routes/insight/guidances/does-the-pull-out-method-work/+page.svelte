		</p>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Timing:</strong> The person with the penis must accurately gauge when they are about
				to ejaculate.
			</li>
			<li>
				<strong>Withdrawal:</strong> Just before ejaculation, the person withdraws their penis from their
				partner's vagina.
			</li>
			<li>
				<strong>Ejaculation:</strong> Ejaculation should occur outside of the vagina, ideally away from
				the genital area.
			</li>
		</ol>
