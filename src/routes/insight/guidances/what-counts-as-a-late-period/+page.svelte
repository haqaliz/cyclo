				/>
			</div>
			<div class="flex flex-col items-start">
				<p class="font-sans text-lg text-justify">
					While occasional late periods are usually nothing to worry about, there are instances when
					you should consult a healthcare provider:
				</p>
				<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
					<li>
						<strong>Consistent Irregular Cycles:</strong>
						<span
							>If your periods are consistently irregular, occurring less than 21 days apart or more
							than 35 days apart, it's advisable to seek medical advice.</span
						>
					</li>
					<li>
						<strong>Severe Pain or Other Symptoms:</strong>
						<span
							>If you experience severe pain, heavy bleeding, or other concerning symptoms along
							with a late period, consult a healthcare professional.</span
						>
					</li>
					<li>
						<strong>Sudden Changes:</strong>
						<span
							>If your periods have been regular and you experience a sudden and unexplained change
							in your cycle, it's a good idea to seek medical guidance.</span
						>
					</li>
					<li>
						<strong>Menopause or Perimenopause:</strong>
						<span
							>Women approaching menopause may experience irregular periods, but it's still
							essential to consult a healthcare provider to rule out any underlying issues.</span
						>
					</li>
					<li>
						<strong>You Suspect Pregnancy:</strong>
						<span
							>If you are sexually active and suspect pregnancy, take a pregnancy test, and consult
							a healthcare provider for confirmation and guidance.</span
						>
					</li>
				</ol>
			</div>
		</div>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Medical Advice for Late Periods</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			During a medical consultation for late periods, your healthcare provider may:
		</p>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Conduct a Pregnancy Test:</strong>
				<span>To rule out or confirm pregnancy.</span>
			</li>
			<li>
				<strong>Perform Hormonal Tests:</strong>
				<span>To assess hormonal imbalances that may be causing irregular periods.</span>
			</li>
			<li>
				<strong>Recommend Lifestyle Changes:</strong>
				<span
					>Such as managing stress, maintaining a healthy weight, or adjusting exercise routines.</span
				>
			</li>
			<li>
				<strong>Prescribe Medications:</strong>
				<span
					>In some cases, hormonal medications or other treatments may be suggested to regulate your
					cycle.</span
				>
			</li>
		</ol>
	</div>
