		<h3 class="font-sans font-semibold text-3xl">Introduction</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			For many individuals, the arrival of their monthly period is a reliable event, signaling the
			natural rhythm of their reproductive cycle. But what happens when that anticipated date comes
			and goes without a sign of menstruation? Is it considered a late period, and when should you
			be concerned? In this blog post, we'll explore what counts as a late period, the potential
			causes behind it, and when it might be time to seek medical advice.
		</p>
