	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Understanding Menstrual Regularity</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			The average menstrual cycle is approximately 28 days, although cycles can vary from person to
			person. A regular menstrual cycle typically falls within a range of 21 to 35 days. Your
			menstrual cycle begins on the first day of your period and ends on the day before your next
			period starts.
		</p>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">What Counts as a Late Period?</h3>
		<p class="font-sans text-lg text-justify mt-2 sm:mt-4">
			A late period is defined as a delay of more than seven days beyond your expected or typical
			menstrual cycle. In essence, if your period consistently arrives within a certain timeframe,
			and it is delayed beyond this window, it is considered late.
		</p>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">Common Causes of a Late Period</h3>
		<ol class="font-sans text-lg list-decimal list-inside mt-2 sm:mt-4">
			<li>
				<strong>Stress:</strong>
				<span
					>High levels of stress, whether due to work, personal life, or other factors, can disrupt
					hormonal balance and lead to a delayed period.</span
				>
			</li>
			<li>
				<strong>Changes in Routine:</strong>
				<span
					>Travel, diet, and changes in daily routines can affect your body's internal clock,
					potentially causing a delay in your period.</span
				>
			</li>
			<li>
				<strong>Illness or Infection:</strong>
				<span>A significant illness or infection can temporarily affect your menstrual cycle.</span>
			</li>
			<li>
				<strong>Extreme Weight Changes:</strong>
				<span
					>Rapid weight loss, excessive exercise, or eating disorders can lead to hormonal
					imbalances and delayed periods.</span
				>
			</li>
			<li>
				<strong>Medical Conditions:</strong>
				<span
					>Conditions such as polycystic ovary syndrome (PCOS) or thyroid disorders can cause
					irregular periods.</span
				>
			</li>
			<li>
				<strong>Medications:</strong>
				<span
					>Some medications, like hormonal contraceptives or certain prescription drugs, can impact
					your menstrual cycle.</span
				>
			</li>
			<li>
				<strong>Pregnancy:</strong>
				<span
					>Pregnancy is one of the most common reasons for a missed period. If you are sexually
					active and your period is late, taking a pregnancy test is advisable.</span
				>
			</li>
		</ol>
	</div>
	<div class="bg-gray-100 rounded p-2 sm:p-4 flex flex-col mt-2 sm:mt-4">
		<h3 class="font-sans font-semibold text-3xl">When to Seek Medical Advice</h3>
		<div class="flex flex-col sm:flex-row items-start mt-2 sm:mt-4">
			<div class="flex flex-col justify-center mb-2 sm:mb-0">
				<div
